/* 
O operador de identidade (===) compara os operandos sem fazer conversão de tipo 
do dado. Já o operador de igualdade (==) converte o operando se ele não for 
do mesmo tipo do outro e só depois faz a comparação estrita.
*/

console.log('3' == 3) // true

console.log('7' == 7) // true

console.log(10 === 10) // true

console.log('5' === 5) // false

console.log(typeof 5)

console.log(typeof '5')